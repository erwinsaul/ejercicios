#include <bits/stdc++.h>

using namespace std;

int main(int argc, char **argv)
{
	int t;
	string s,a,b;
	cin>>t;
	for(int k=1;k<=t;k++)
	{
		getline(cin, s);
		getline(cin, a);
		getline(cin, b);
		cout<<s<<endl;
		cout<<a<<endl;
		cout<<b<<endl;
	}
	return 0;
}

